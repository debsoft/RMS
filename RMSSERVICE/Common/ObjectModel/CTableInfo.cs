using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CTableInfo
    {

        private int m_iTableNumber;
        private int m_iCapacity;
        private int m_iStatus;


        public CTableInfo()
        {
            m_iTableNumber = 0;
            m_iCapacity = 0;
            m_iStatus = 0;

        }


        public int TableNumber
        {
            get { return m_iTableNumber; }
            set { m_iTableNumber = value; }
        }


        public int Capacity
        {
            get { return m_iCapacity; }
            set { m_iCapacity = value; }
        }


        public int Status
        {
            get { return m_iStatus; }
            set { m_iStatus = value; }
        }



    }
}
