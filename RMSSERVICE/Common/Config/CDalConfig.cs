using System;
using System.Collections.Generic;
using System.Text;

namespace RMS
{
    namespace Common
    {
        namespace Config
        {
            public class CDalConfig
            {
                private string m_sConfigName;
                private string m_sProvider;
                private string m_sConnectionString;
                private string m_sPoolType;
                private string m_sInOutboxConnectionString;

                public CDalConfig()
                {
                    m_sConfigName = String.Empty;
                    m_sProvider = String.Empty;
                    m_sConnectionString = String.Empty;
                    m_sPoolType = String.Empty;
                    m_sInOutboxConnectionString = String.Empty;
                }


                public string ConfigName
                {
                    set { m_sConfigName = value; }
                    get { return m_sConfigName; }
                }

                public string Provider
                {
                    set { m_sProvider = value; }
                    get { return m_sProvider; }
                }

                public string ConnectionString
                {
                    set { m_sConnectionString = value; }
                    get { return m_sConnectionString; }
                }

                public string PoolType
                {
                    set { m_sPoolType = value; }
                    get { return m_sPoolType; }
                }

                public string InOutboxConnectionString
                {
                    get { return m_sInOutboxConnectionString; }
                    set { m_sInOutboxConnectionString = value; }
                }
            }//CDalConfig
        }//ns
    }//ns
}//ns
