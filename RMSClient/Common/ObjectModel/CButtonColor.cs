using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CButtonColor
    {
        private int m_iButtonID;
		private String m_sButtonName;
        private String m_sButtonColor;
            
		public CButtonColor()
		{
            m_iButtonID = 0;
            m_sButtonName = String.Empty;
            m_sButtonColor = String.Empty;            
		}

        public int ButtonID
		{
            get { return m_iButtonID; }
            set { m_iButtonID = value; }
		}

        public String ButtonName
		{
            get { return m_sButtonName; }
            set { m_sButtonName = value; }
		}

        public String ButtonColor
		{
            get { return m_sButtonColor; }
            set { m_sButtonColor = value; }
		} 
    }
}
