using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class GeneralSettings
    {
        private double vat;
        private bool isVatEnabled;
        private string currency;
        
        public GeneralSettings()
        { 
        
        }

        public double Vat {

            get { return this.vat;}
            set { vat = value; }
        }
        public bool IsVatEnabled
        {

            get { return this.isVatEnabled; }
            set { isVatEnabled = value; }
        }
        public string Currency
        {

            get { return this.currency; }
            set { currency = value; }
        }

    }
}
