using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CDepositUsed
    {
        private Int64 m_iDepositUseID;
        private Int64 m_iDepositID;
        private Double m_fUsedAmount;

        public CDepositUsed()
        {
            m_iDepositUseID = 0;
            m_iDepositID = 0;
            m_fUsedAmount = 0.00;
        }

        public Int64 DepositUseID
        {
            get { return m_iDepositUseID; }
            set { m_iDepositUseID = value; }
        }

        public Int64 DepositID
        {
            get { return m_iDepositID; }
            set { m_iDepositID = value; }
        }

        public Double UsedAmount
        {
            get { return m_fUsedAmount; }
            set { m_fUsedAmount = value; }
        }
    }
}
