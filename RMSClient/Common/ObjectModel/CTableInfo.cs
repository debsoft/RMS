using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CTableInfo
    {

        private Int64 m_iTableNumber;
        private Int64 m_iWaitingNumber;
        private int m_iCapacity;
        private int m_iStatus;
        private string m_iTableType;

        public CTableInfo()
        {
            m_iTableNumber = 0;
            m_iCapacity = 0;
            m_iStatus = 0;
            m_iTableType = string.Empty;
        }

        public Int64 WaitingNumber
        {
            get { return m_iWaitingNumber; }
            set { m_iWaitingNumber = value; }
        }


        public Int64 TableNumber
        {
            get { return m_iTableNumber; }
            set { m_iTableNumber = value; }
        }


        public int Capacity
        {
            get { return m_iCapacity; }
            set { m_iCapacity = value; }
        }


        public int Status
        {
            get { return m_iStatus; }
            set { m_iStatus = value; }
        }
        public string TableType
        {
            get { return m_iTableType; }
            set { m_iTableType = value; }
        }


    }
}
