using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CDeposit
    {
        private Int64 m_bDepositID;
        private double m_fBalance;
        private Int64 m_iDepositTime;
        private Int64 m_iCustomerID;
        private double m_fTotalAmount;
        private int m_iPcID;
        private int m_iStatus;
        private String m_sType;

        public CDeposit()
        {
            m_bDepositID = 0;
            m_fBalance = 0;
            m_iDepositTime = 0;
            m_iCustomerID = 0;
            m_fTotalAmount = 0;
            m_iPcID = 0;
            m_iStatus = 0;
            m_sType = String.Empty;
        }

        public Int64 DepositID
        {
            get { return m_bDepositID; }
            set { m_bDepositID = value; }
        }

        
        public double DepositBalance
        {
            get { return m_fBalance; }
            set { m_fBalance = value; }
        }


        public Int64 DepositTime
        {
            get { return m_iDepositTime; }
            set { m_iDepositTime = value; }
        }
        public Int64 CustomerID
        {
            get { return m_iCustomerID; }
            set { m_iCustomerID = value; }
        }

        public double DepositTotalAmount
        {
            get { return m_fTotalAmount; }
            set { m_fTotalAmount = value; }
        }

        public int PcID
        {
            get { return m_iPcID; }
            set { m_iPcID = value; }
        }

        public int Status
        {
            get { return m_iStatus; }
            set { m_iStatus = value; }
        }

        public String DepositType
        {
            get { return m_sType; }
            set { m_sType = value; }
        }
    }
}
