using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CKitchenPrint
    {
        private Int64 m_bPrintID;
        private Int64 m_bOrderID;
        private DateTime m_oPrintTime;
        private int m_iPCID;

        public CKitchenPrint()
        {
            m_bPrintID = 0;
            m_bOrderID = 0;
            m_oPrintTime = new DateTime();
            m_iPCID = 0;
        }

        public Int64 KitchenPrintID
        {
            get { return m_bPrintID; }
            set { m_bPrintID = value; }
        }

        public Int64 KitchenPrintOrderID
        {
            get { return m_bOrderID; }
            set { m_bOrderID = value; }
        }

        public DateTime KitchenPrintTime
        {
            get { return m_oPrintTime; }
            set { m_oPrintTime = value; }
        }

        public int KitchenPrintPCID
        {
            get { return m_iPCID; }
            set { m_iPCID = value; }
        }


    }
}
