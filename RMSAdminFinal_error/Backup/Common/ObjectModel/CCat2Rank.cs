using System;
using System.Collections.Generic;
using System.Text;

namespace RMS.Common.ObjectModel
{
    public class CCat2Rank
    {
        private int m_iCategory2ID;
        private int m_iCategory2Order;

        private List<CCat3Rank> m_oChildCat3List;

        private List<CCat4Rank> m_oChildCat4List;


        public CCat2Rank()
        {
            m_iCategory2ID = 0;

            m_iCategory2Order = 0;

            m_oChildCat3List = new List<CCat3Rank>();

            m_oChildCat4List = new List<CCat4Rank>();

        }

        public int Category2ID
        {
            get { return m_iCategory2ID; }
            set { m_iCategory2ID = value; }
        }

        public int Category2Order
        {
            get { return m_iCategory2Order; }
            set { m_iCategory2Order = value; }
        }

        public List<CCat3Rank> ChildCat3List
        {
            get { return m_oChildCat3List; }
            set { m_oChildCat3List = value; }
        }

        public List<CCat4Rank> ChildCat4List
        {
            get { return m_oChildCat4List; }
            set { m_oChildCat4List = value; }
        }



    }
}
